function showBasket(fruits) {
  let frutas = {}
  let quantidades = [];

  for (let index = 0; index < fruits.length; index += 1) {
    let frutaAtual = fruits[index]
    if (!frutas[frutaAtual]) frutas[frutaAtual] = 0;
    frutas[frutaAtual] += 1;
  }

  for (let fruta in frutas) {
    let total = frutas[fruta];
    let msg = `${total} ${fruta}s`
    quantidades.push(msg)
  }

  return `Sua cesta possui: ${quantidades.join(', ')}`
}

